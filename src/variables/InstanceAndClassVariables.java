package variables;

public class InstanceAndClassVariables {
    /*
    Instance dəyişənlər həm də “fields” adlandırılır. Instance və class dəyişənlər lokal dəyişənlərdən fərqli
olaraq classın daxilində, amma metodun xaricində elan edilir. Lokal dəyişənlərdən digər
fərqləri isə odur ki, bu növ dəyişənlər istifadə edilmədən öncə dəyər mənimsədilməsi
(initialize) tələb etmir. Instance və class dəyişənlər artıq elan olunan vaxtı onlara susmaya
görə (default) dəyər mənimsədilir. Susmaya görə dəyərlərlə aşağıdakı cədvəldən tanış ola
bilərsiniz:
boolean - false
byte, short, int, long -0
float, double - 0.0
char  - '\u0000'
Bütün obyekt tipləri üçün - null

Class dəyişənləri ilə instance dəyişənlərinin fərqi isə odur ki, class dəyişənlərin önündə static
açar sözü yazılır, buna görə də bu dəyişənlər həm də static dəyişənlər adlandırılır. Static
dəyişənlər classın instansını yaratmadan birbaşa class adının özü ilə müraciət etməklə digər
classlarda çağırıla bilər.

Variable Scope:

Dəyişənlərin təsir dairəsi (variable scope) bu formadadır:
• Lokal dəyişənlər – elan olunduğu sətirdən daxilində olduğu kod bloku bitənədək;
• Instance dəyişənlər – elan olunduğu vaxtdan GC tərəfindən silinənədək;
• Class dəyişənlər – elan olunduğu vaxtdan proqram sonlananadək.
*/

}
